"""
Курсы валют для сравнения с порогом зарплаты. ДАННЫЕ, а не логика.

Зачем это вообще. 10% постов-вакансий (замер 2026-07-15: 40 из 390) дают
зарплату в долларах — это международная удалёнка, ровно то, что нужно. Пока
конвертацию делал ИИ, она была лотереей: один и тот же "от 2 500 $" стал то
2 500 000 руб (умножил на 1000 вместо курса), то честными 2500 USD. А правило
валюту не смотрело и сравнивало "2500 < 230000" — вакансия за ~200к руб
вылетала молча.

Поэтому: ИИ ИЗВЛЕКАЕТ ("2500", "USD"), КОД КОНВЕРТИРУЕТ. Модель не знает
сегодняшний курс и знать не может, а детерминизм тут важнее точности: $3000
обязаны давать один ответ, а не зависеть от настроения модели.

Курс приблизительный и ПРОТУХАЕТ — это осознанно. Он нужен для сравнения с
порогом ("больше 230к или нет"), а не для бухгалтерии: промах в 10% на
границе не меняет решения, зато отличает 200к от 2.5 млн. Устарел на треть —
поправь руками.
"""

# Обновлено 2026-07-15
RATES_TO_RUB: dict[str, float] = {
    "RUB": 1.0,
    "USD": 80.0,
    "USDT": 80.0,  # стейблкоин 1:1 к доллару, в постах встречается ("до 3000 USDT")
    "EUR": 90.0,
    "KZT": 0.17,
    "BYN": 25.0,
}


def to_rub(value: int | None, currency: str | None) -> int | None:
    """
    Сумма в рублях. None на входе -> None на выходе (неизвестно != ноль).

    Валюта не указана -> считаем рублями: в этих каналах рубль по умолчанию,
    и почти всегда так и есть.

    Валюта незнакомая -> тоже None, а НЕ сырое число. Вернуть 3000 условных
    единиц как "3000 рублей" значит соврать правилу, и оно молча выкинет
    вакансию. None честнее: неизвестная зарплата на отбор не влияет и уедет
    на ИИ, который прочитает текст поста.
    """
    if value is None:
        return None
    if currency is None:
        return value
    rate = RATES_TO_RUB.get(currency.strip().upper())
    if rate is None:
        return None
    return int(value * rate)
